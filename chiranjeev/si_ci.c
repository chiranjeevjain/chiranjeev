#include<stdio.h>
#include<math.h>
void main()
{
	float p,r,t;
	float si,ci;
	printf("\n Enter principal amount ");
	scanf("%f",&p);
	printf("\n Enter rate of interest ");
	scanf("%f",&r);
	printf("\n enter time ");
	scanf("%f",&t);
	si= (p*r*t)/100;
	//ci = p*(pow((1+r),t)-1);
	ci = p*pow(1+r/100,t);
	printf("\n simple interest is %f",si);
	printf("\n compound interest is %f \n ",ci);
	
	
	
}
